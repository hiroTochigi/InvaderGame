unsigned int _is_bullet_hit_against_player(struct sprite bullet, struct sprite player){
	
	return player.life &&
				 bullet.Image[18] == MISSILEW &&
				 bullet.life == 1 &&
				 bullet.ypos > player.ypos - PLAYERH + 2 &&
				 player.xpos + 2 < bullet.xpos && bullet.xpos < player.xpos + PLAYERW - 2;
}


unsigned char _get_sprite_wide(const unsigned char* image){
	
	unsigned char test_subject = image[18];
	if(test_subject == ENEMY30W){
		return ENEMY30W;
	}else if(ENEMY20W){
		return ENEMY20W;
	}else if(ENEMY10W){
		return ENEMY10W;
	}else if(ENEMYBONUSW){
		return ENEMYBONUSW;
	}else if(BUNKERW){
		return BUNKERW;
	}
	return 0;
}

unsigned int _is_bullet_hit_against_enemy(struct sprite bullet, struct sprite enemy){
	
	int _is_enemy_alive = enemy.life;
	int _is_leser = bullet.Image[18] == LASERW && bullet.life == 1;
	int _is_bullet_within_enemy = bullet.ypos < enemy.ypos + 2 &&
				 enemy.xpos + 2 < bullet.xpos && bullet.xpos < enemy.xpos + _get_sprite_wide(enemy.Image) - 2;
	return _is_enemy_alive && _is_leser && _is_bullet_within_enemy;	 
}

unsigned int _is_bullet_hit_against_bunker(struct sprite bullet, struct sprite bunker){
	
	int _is_bunker_alive = bunker.life;
	int _is_bullet_alive = bullet.life > 0;
	int _is_bullet_x_val_inside_bunker = bunker.xpos + 2 < bullet.xpos && bullet.xpos < bunker.xpos + _get_sprite_wide(bunker.Image) - 2;
	int _is_enemy_bullet_y_val_inside_bunker = bullet.ypos >= bunker.ypos && !_is_laser(bullet);
	int _is_player_bullet_y_val_inside_bunker = bullet.ypos <= bunker.ypos && _is_laser(bullet);
	
	return _is_bunker_alive && _is_bullet_alive && _is_bullet_x_val_inside_bunker && ( _is_enemy_bullet_y_val_inside_bunker || _is_player_bullet_y_val_inside_bunker );	 
}

unsigned int _is_laser(struct sprite bullet){
	if (bullet.Image == Laser0 || bullet.Image == Laser1){
		return 1;
	}
	return 0;
}

struct sprite_set Add_Collision_Detection_Effect(struct sprite_set All_Sprite_Array_Set){
	
	int current_bullet;
	int current_explosion;
	int current_enemy;
	int current_bunker;
	
	for(current_bullet=0; current_bullet<BULLET_SIZE; current_bullet++){
		if (_is_bullet_hit_against_player(All_Sprite_Array_Set.BulletArray[current_bullet], All_Sprite_Array_Set.Player[0])){
			All_Sprite_Array_Set.BulletArray[current_bullet].life = 0;
			All_Sprite_Array_Set.BulletArray[current_bullet].ypos = 0;
			All_Sprite_Array_Set.Player[0].life = 0;
			for(current_explosion=0; current_explosion<EXPLOSION_SIZE; current_explosion++){
				if(All_Sprite_Array_Set.ExplosionArray[current_explosion].life == 0){
					All_Sprite_Array_Set.ExplosionArray[current_explosion].Image = BigExplosion0;
					All_Sprite_Array_Set.ExplosionArray[current_explosion].xpos = All_Sprite_Array_Set.Player[0].xpos;
					All_Sprite_Array_Set.ExplosionArray[current_explosion].ypos = All_Sprite_Array_Set.Player[0].ypos;
					All_Sprite_Array_Set.ExplosionArray[current_explosion].life = 15;
					break;
				}
			}
		}
		for(current_enemy=0; current_enemy<ENEMY_SIZE; current_enemy++){
			if(_is_bullet_hit_against_enemy(All_Sprite_Array_Set.BulletArray[current_bullet], All_Sprite_Array_Set.EnemyArray[current_enemy])){
				All_Sprite_Array_Set.BulletArray[current_bullet].life = 0;
				All_Sprite_Array_Set.BulletArray[current_bullet].ypos = 0;
				All_Sprite_Array_Set.EnemyArray[current_enemy].life = 0;
				for(current_explosion=0; current_explosion<EXPLOSION_SIZE; current_explosion++){
					if(All_Sprite_Array_Set.ExplosionArray[current_explosion].life == 0){
						All_Sprite_Array_Set.ExplosionArray[current_explosion].Image = SmallExplosion0;
						All_Sprite_Array_Set.ExplosionArray[current_explosion].xpos = All_Sprite_Array_Set.EnemyArray[current_enemy].xpos;
						All_Sprite_Array_Set.ExplosionArray[current_explosion].ypos = All_Sprite_Array_Set.EnemyArray[current_enemy].ypos;
						All_Sprite_Array_Set.ExplosionArray[current_explosion].life = 15;
						break;
					}
				}
			}
		}
		for(current_bunker=0; current_bunker<BUNKER_SIZE; current_bunker++){
			if(_is_bullet_hit_against_bunker(All_Sprite_Array_Set.BulletArray[current_bullet], All_Sprite_Array_Set.BunkerArray[current_bunker])){
				All_Sprite_Array_Set.BulletArray[current_bullet].life = 0;
				All_Sprite_Array_Set.BulletArray[current_bullet].ypos = 0;
				
				All_Sprite_Array_Set.BunkerArray[current_bunker].life--;
				All_Sprite_Array_Set.BunkerArray[current_bunker].Image = Get_Next_Sprite(All_Sprite_Array_Set.BunkerArray[current_bunker].Image);
				bunker_life = All_Sprite_Array_Set.BunkerArray[current_bunker].life;
			}
		}
	}
	
	return All_Sprite_Array_Set;
}

