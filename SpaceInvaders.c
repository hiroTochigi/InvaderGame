int Return_Next_Move_Num(int biggest_move, int threshold, int current_pos){
	int diff = current_pos - threshold;
	int glanular_move = 0;
	if (diff < 0){
		diff *= -1;
	}
	if(current_pos == threshold){
		return 0;
	}
	if (biggest_move <= diff){
		return biggest_move;
	}else{
		glanular_move = biggest_move - diff - 1;
		return glanular_move;
	}
}
